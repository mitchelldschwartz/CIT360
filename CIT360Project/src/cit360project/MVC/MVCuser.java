public class Employee {
   private String emplID;
   private String name;
   
   public String getemplID() {
      return emplID;
   }
   
   public void setemplID(String emplID) {
      this.emplID = emplID;
   }
   
   public String getName() {
      return name;
   }
   
   public void setName(String name) {
      this.name = name;
   }
}